namespace GrooveBox.DragDropper
{
    /// <summary>
    /// The states of a drop.
    /// </summary>
    public enum DropState
    {
        /// <summary>
        /// Is allowed to be dropped.
        /// </summary>
        CanDrop = 1,

        /// <summary>
        /// Is not allowed to be dropped.
        /// </summary>
        CannotDrop = 2
    }
}